{
    void init(int[][4]);
    bool comparison(int[][4], int[][4]);
    void diagonalize(int[][4]);
    void printMatrix(int[][4]);

    int A[4][4];

    int B[4][4];

    if (comparison(A, B))
    {
        diagonalize(A);
        printMatrix(A);
    }
    else
        cout << "Matrices are not equal" << endl;

    return 0;
}

void diagonalize(int matrix[][4])
{
    for (int i = 0; i < 4; i++)
        for (int j = 0; j < 4; j++)
            if (i != j)
                matrix[i][j] = 0;
}

bool comparison(int A[][4], int B[][4])
{
    for (int i = 0; i < 4; i++)
        for (int j = 0; j < 4; j++)
            if (A[i][j] != B[i][j])
                return false;

    return true;
}

void initMatrix(int matrix[][4])
{
    for (int i = 0; i < 4; i++)
        for (int j = 0; j < 4; j++)
            cin >> matrix[i][j];
}

void printMatrix(int matrix[][4])
{
    for (int i = 0; i < 4; i++)
    {
        for (int j = 0; j < 4; j++)
            cout << matrix[i][j] << " ";
        cout << endl;
    }
}
